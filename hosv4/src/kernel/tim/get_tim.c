#include "knl_tim.h"



/* システム時刻の参照 */
ER get_tim(
		SYSTIM *p_systim)		/* システム時刻に設定する時刻 */
{
	mknl_loc_sys();		/* システムのロック */

	/* システム時刻の取得 */
	*p_systim = kernel_systim;

	mknl_unl_sys();		/* システムのロック解除 */

	return E_OK;
}


